type SequenceStep = readonly [
  midi: number | null,
  beats: number,
  velocity?: number,
];

type ChordStep = readonly [
  midis: readonly number[],
  beats: number,
  velocity?: number,
];

const round = (value: number) => Math.round(value * 10_000) / 10_000;
const secondsPerBeat = (bpm: number) => 60 / bpm;

/** Turn a monophonic beat-based line into renderer-ready note timings. */
const noteSequence = (
  prefix: string,
  bpm: number,
  hand: Hand,
  steps: readonly SequenceStep[],
  startBeat = 0,
  gate = 0.9,
): SongNote[] => {
  const beatLength = secondsPerBeat(bpm);
  const notes: SongNote[] = [];
  let cursor = startBeat;

  for (const [midi, beats, velocity = 84] of steps) {
    if (midi !== null) {
      notes.push({
        id: `${prefix}-${hand}-${String(notes.length + 1).padStart(3, "0")}`,
        midi,
        time: round(cursor * beatLength),
        duration: round(beats * beatLength * gate),
        velocity,
        hand,
      });
    }
    cursor += beats;
  }

  return notes;
};

/** Turn block chords or bass notes into a timed accompaniment line. */
const chordSequence = (
  prefix: string,
  bpm: number,
  hand: Hand,
  steps: readonly ChordStep[],
  startBeat = 0,
  gate = 0.88,
): SongNote[] => {
  const beatLength = secondsPerBeat(bpm);
  const notes: SongNote[] = [];
  let cursor = startBeat;

  for (const [midis, beats, velocity = 72] of steps) {
    for (const midi of midis) {
      notes.push({
        id: `${prefix}-${hand}-${String(notes.length + 1).padStart(3, "0")}`,
        midi,
        time: round(cursor * beatLength),
        duration: round(beats * beatLength * gate),
        velocity,
        hand,
      });
    }
    cursor += beats;
  }

  return notes;
};

/** Reuse a phrase without duplicating note data; useful for forms such as A–A. */
const repeatSteps = (
  pattern: readonly SequenceStep[],
  times: number,
  transpose = 0,
): SequenceStep[] =>
  Array.from({ length: times }, () =>
    pattern.map(
      ([midi, beats, velocity]) =>
        [midi === null ? null : midi + transpose, beats, velocity] as const,
    ),
  ).flat();

const mergeNotes = (...groups: SongNote[][]): SongNote[] =>
  groups
    .flat()
    .sort((a, b) => a.time - b.time || a.midi - b.midi || a.id.localeCompare(b.id));

const songDuration = (bpm: number, beats: number) =>
  round(beats * secondsPerBeat(bpm) + 0.8);

